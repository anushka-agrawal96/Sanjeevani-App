    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        if(orderList.size()==0){
             JOptionPane.showMessageDialog(null, "No Item in cart");
             return;
         }
             
         int index=jtProductDetails.getSelectedRow();
         if(index==-1){
             JOptionPane.showMessageDialog(null, "Please Select a item");
             return;
         }
          OrderPojo ord=orderList.get(index);
       try{
          String staffId=StaffDao.getRandomStaffId(ord.getCompayId());
          int otp =OrderDao.orderInCart(ord.getOrderId(), staffId);
          if(otp==0){
             JOptionPane.showMessageDialog(null,"Item is not order please try Again");
              return;
          }
          String [] details=OrderDao.getInformationCompanyStaff(ord.getCompayId(), staffId);
          ord.setCompanyName(details[0]);
          ord.setCompanyEmailId(details[1]);
          ord.setDeliveryStaffName(details[2]);
          ord.setOtp(otp);
          Map<String,String>emailCredentials=CompanyDao.getEmailCredentialByCompanyId(ord.getCompayId());
          Mailer.sendMail(emailCredentials, ord);
          JOptionPane.showMessageDialog(null,"Order placed and Email Sent Successfully to:"+UserProfile.getEmailId());
          model.removeRow(index);
          }
      catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB ERROR IN MyCart Frame");
            sq.printStackTrace();
        }
       catch(MessagingException sq){
            JOptionPane.showMessageDialog(null,"MAIL ERROR IN MyCartFrame");
            sq.printStackTrace();
        }
    }//GEN-LAST:event_btnOrderActionPerformed

