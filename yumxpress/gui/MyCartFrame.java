    private void loadFoodDetails() {
        try{
            orderList = OrderDao.getOrdersInCartForCustomer(UserProfile.getUserId());
            if(orderList.size()==0){
                JOptionPane.showMessageDialog(null,"No items for cart");
                return;
            }
            Object [] order = new Object[2];
            for(OrderPojo ord:orderList){
                order[0] = ord.getProductName();
                order[1] = ord.getProductPrice();
                model.addRow(order);
            }
        }
        catch(SQLException sq){
            JOptionPane.showMessageDialog(null, "Error in DB in MyCartFrame");
            sq.printStackTrace();
        }
        
    }
