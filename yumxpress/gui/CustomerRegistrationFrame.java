    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        if(!validInputs()){
            return;
        }
        try{
            CustomerPojo cust = new CustomerPojo();
            
            cust.setCustomerName(txtCustomerName.getText().trim());
            cust.setEmailId(txtEmail.getText().trim());
            cust.setMobileNo(txtMobileNo.getText().trim());
               cust.setAddress(txtAddress.getText().trim());
            char [] pwd = txtPassword.getPassword();
            String pwdstr = String.valueOf(pwd);
            pwdstr = PasswordEncryption.getEncryptedPassword(pwdstr);
            cust.setPassword(pwdstr);
            boolean result = CustomerDao.addCustomer(cust);
            if(result){
                JOptionPane.showMessageDialog(null,"Registration Successfull");
                Map<String,String> emailCredentials = new HashMap<>();
                emailCredentials.put("emailId","yumxpress12@gmail.com");
                emailCredentials.put("securityKey","aphs dpte roqb sgbt");
                Mailer.sendMail(emailCredentials,cust);
                JOptionPane.showMessageDialog(null,"Mail sent to "+cust.getEmailId());
                clearText();
                return;
            }
            JOptionPane.showMessageDialog(null,"Registration UnSuccessfull");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error in CustomerRegistratioFrmae");
            ex.printStackTrace();
        }
        catch(MessagingException ex){
            JOptionPane.showMessageDialog(null,"Mail Error in CustomerRegistraionFrmae");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

