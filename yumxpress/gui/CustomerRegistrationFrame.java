private boolean validInputs() {
        char [] pwd = txtPassword.getPassword();
        if(pwd.length==0 || txtCustomerName.getText().trim().isEmpty()|| txtMobileNo.getText().trim().isEmpty()||txtEmail.getText().trim().isEmpty()||txtAddress.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the data");
            return false;
        }
        if(Validator.isValidEmail(txtEmail.getText().trim())==false){
            JOptionPane.showMessageDialog(null,"Please input valid email id");
            return false;
        }
        if(pwd.length<8){
            JOptionPane.showMessageDialog(null,"password should be atleast 8 digit long");
            return false;
        }
        if(Validator.isValidMobile(txtMobileNo.getText().trim())==false){
            JOptionPane.showMessageDialog(null,"Please input valid mobile number");
            return false;
        }
        return true;
    }

    private void clearText() {
        txtCustomerName.setText("");
        txtMobileNo.setText("");
        txtEmail.setText("");
        txtPassword.setText("");
        txtAddress.setText("");
        txtEmail.requestFocus();
    }
