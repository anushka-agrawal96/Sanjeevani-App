    private void jcCompanyNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcCompanyNameActionPerformed
        String compName = jcCompanyName.getSelectedItem().toString();
        String compId = companyMap.get(compName);
        if(compId==null){
            compId = "ALL";
        }
        try{
            productList = ProductDao.getAllProductByCompanyId(compId);
            showDetails();
        }
        catch(SQLException sq){
            JOptionPane.showMessageDialog(null, "Error in DB in orderFoodFrame");
            sq.printStackTrace();
        }
        catch(IOException ex){
            JOptionPane.showMessageDialog(null, "Image Error in DB in orderFoodFrame");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcCompanyNameActionPerformed

    private void lblOrderPlaced(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblOrderPlaced
        JLabel lbl = (JLabel) evt.getSource();
        int index = Integer.parseInt(lbl.getName());
        System.out.println("You clicked on:" + index);
        if (index >= productList.size()) {
            JOptionPane.showMessageDialog(null, "Invalid item clicked");
            return;
        }
        String[] str = {"Add To Cart", "Buy Now"};
        int choice = JOptionPane.showOptionDialog(null, "What do you want to do ?", "Select", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, str, str[1]);
        if (choice == 0) {
            try{
                ProductPojo p = productList.get(index);
                PlaceOrderPojo product = new PlaceOrderPojo();
                product.setCompanyId(p.getCompanyId());
                product.setCustomerId(UserProfile.getUserId());
                product.setProductId(p.getProductId());
                String ordId = OrderDao.placeOrderInCart(product);
                if(ordId==null){
                    JOptionPane.showMessageDialog(null, "Sorry! Order can not be placed");
                    return;
                }
                JOptionPane.showMessageDialog(null, "Item added in cart");
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error in DB in orderFoodFrame");
                ex.printStackTrace();
            }
            showFrame = new MyCartFrame();
            showFrame.setVisible(true);
            this.dispose();
             
        } else {
            ProductPojo p = productList.get(index);
            showFrame = new OrderProductFrame(p);
            showFrame.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_lblOrderPlaced

