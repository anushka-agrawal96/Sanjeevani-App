    private void loadCompanyName() {
        try{
            companyMap = CompanyDao.getAllCompanyIdAndName();
            Set<String> companyName = companyMap.keySet();
            jcCompanyName.addItem("ALL");
            for(String cname:companyName){
                jcCompanyName.addItem(cname);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB in orderFoodFrame");
            ex.printStackTrace();
        }
    }

